package com.syntax.class02;

public class ArithmericOperators {

	public static void main(String[] args) {
		
		//+,-,*,/,%
		
		int a=900;
		int b=100;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		int c=10;
		int d=3;
		
		int sum=c+d;
		int sub=c-d;
		int remainder=c%d;
		
		int div=c/d;
		System.out.println(sum);
		System.out.println(sub);
		
		int e=10;
		int f=5;
		
		System.out.println("division = " +e/f);
		System.out.println("remainder = "+e%f);
		System.out.println(remainder);
		System.out.println("Div is = "+div);//keeping the whole number only because it is int, no decimals, only whole numbers
		
		double num1=10;
		double num2=3;
		
		System.out.println(num1/num2);
		
		float number1=10.0f;
		float number2=3.0f;
		
		System.out.println(number1/number2);
		
		
		
		
		
		
		
	}

}
