public class forLoopDemo2 {

	public static void main(String[] args) {
		
		//i want to print this pattern 10 8 6 4 2 0
		for(int i=10;i>=0; i-=2) {
			System.out.println(i);
		}
		
		//i want to print 9 5 1
		System.out.println("====================");
		for(int i=9;i>=1;i-=4) {
			System.out.println(i);
		}
		
		System.out.println("=====================");
		
	}

}
