package com.syntax.class07;

public class ForLoop {

	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++) {
			System.out.println("Hello");
		}
		
		System.out.println("-------------------------");
		
		//print numbers from 1 to 20
		
		for(int a=1;a<=20;a++) {
			System.out.print(a+" ");
		}
		System.out.println("-------------------------");
		
		//print numbers from 30 to 10
		
		for(int b=30;b>=10;b--) {
			System.out.print(b+" ");
		}
		
		
	}

}
